CREATE TABLE SPRINT (
    ID NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    NAME VARCHAR2(100) NOT NULL,
    START_DATE DATE NOT NULL,
    END_DATE DATE NOT NULL,
    STATUS VARCHAR2(20) DEFAULT 'PLANNED',
    CREATED_BY NUMBER,
    CREATION_TS TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE TASK_HISTORY
   (	ID NUMBER GENERATED ALWAYS AS IDENTITY MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 1 CACHE 20 NOORDER  NOCYCLE  NOKEEP  NOSCALE , 
	TASK_ID NUMBER, 
	UPDATED_BY NUMBER, 
	OLD_STATUS VARCHAR2(50 BYTE) COLLATE "USING_NLS_COMP", 
	NEW_STATUS VARCHAR2(50 BYTE) COLLATE "USING_NLS_COMP", 
	OLD_ASSIGNED_TO NUMBER, 
	NEW_ASSIGNED_TO NUMBER, 
	CHANGED_AT TIMESTAMP (6) DEFAULT CURRENT_TIMESTAMP
   )  DEFAULT COLLATION "USING_NLS_COMP" ;


CREATE TABLE USERS 
   (	ID NUMBER GENERATED ALWAYS AS IDENTITY MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 1 CACHE 20 NOORDER  NOCYCLE  NOKEEP  NOSCALE , 
	USERNAME VARCHAR2(100 BYTE) COLLATE "USING_NLS_COMP", 
	PASSWORD VARCHAR2(255 BYTE) COLLATE "USING_NLS_COMP", 
	ROLE VARCHAR2(10 BYTE) COLLATE "USING_NLS_COMP", 
	NAME VARCHAR2(255 BYTE) COLLATE "USING_NLS_COMP", 
	PHONE VARCHAR2(20 BYTE) COLLATE "USING_NLS_COMP"
   )  DEFAULT COLLATION "USING_NLS_COMP" ;